package program;

public class Swap2VarUsing3var {



public static void main(String[] args) {
	  int a=20, b= 30;
	  int t;   //here t var has no value
	  t=a;     //a var value 20 get stored in t
	  a=b;     // b var value 30 get stored in a
	  b=t;     //t var value 20 get stored in b
	  System.out.println("a=" +a);
	  System.out.println("b=" +b);
	  

}}
